package com.sundyn.entity;

public class City
{
    private int id;
    private String name;
    private String citynum;
    
    public int getId() {
        return this.id;
    }
    
    public void setId(final int id) {
        this.id = id;
    }
    
    public String getName() {
        return this.name;
    }
    
    public void setName(final String name) {
        this.name = name;
    }
    
    public String getCitynum() {
        return this.citynum;
    }
    
    public void setCitynum(final String citynum) {
        this.citynum = citynum;
    }
}
