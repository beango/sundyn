package com.sundyn.vo;

public class PlayVo
{
    Integer playId;
    String playName;
    String playType;
    String playSource;
    String playUpdateDate;
    String playTimes;
    String playIndex;
    
    public Integer getPlayId() {
        return this.playId;
    }
    
    public void setPlayId(final Integer playId) {
        this.playId = playId;
    }
    
    public String getPlayName() {
        return this.playName;
    }
    
    public void setPlayName(final String playName) {
        this.playName = playName;
    }
    
    public String getPlayType() {
        return this.playType;
    }
    
    public void setPlayType(final String playType) {
        this.playType = playType;
    }
    
    public String getPlaySource() {
        return this.playSource;
    }
    
    public void setPlaySource(final String playSource) {
        this.playSource = playSource;
    }
    
    public String getPlayUpdateDate() {
        return this.playUpdateDate;
    }
    
    public void setPlayUpdateDate(final String playUpdateDate) {
        this.playUpdateDate = playUpdateDate;
    }
    
    public String getPlayTimes() {
        return this.playTimes;
    }
    
    public void setPlayTimes(final String playTimes) {
        this.playTimes = playTimes;
    }
    
    public String getPlayIndex() {
        return this.playIndex;
    }
    
    public void setPlayIndex(final String playIndex) {
        this.playIndex = playIndex;
    }
}
